                <h2> Hard Drive Defragmenting </h2>
                <h3>What is fragmentation/defragmenting?</h3>
                <p>
                    Fragmentation is when a file is saved in two different location on the hard drive.
                    This means if you want to load said file the hard drive would have to move between
                    those two locations. This will slow down the hard drive and cause programs to load
                    slower. Defragmenting is a process that moves all parts on the file next to each
                    other, making it load faster. Regular defragmenting is required to keep your system
                    from slowing down too much over time.
                </p>
                <h3>How often should I defrag?</h3>
                <p>
                    Usually you should defrag every 1 to 5 months.
                    If you are doing a lot of hard drive operations (Large file transfers, Large downloads,
                    Installing/Uninstalling)then do it more often.
                </p>
                <h3>Defragmenting is slow.</h3>
                <p>
                    While defragmenting your computer will be slow because it is doing a lot of hard drive
                    operations. If you haven't defragmented your drive in a year, defragmenting will
                    probably take 4 to 8 hours. If you haven't defragmented in a week, then it will take
                    30 minutes to a hour. This dependent on your hard drive speed. It is best to start 
                    a defragment right before you are going to leave for awhile, or before you go to sleep.
                </p>
